// Converts a QUL (Quranic Universal Library) per-reciter word-timestamp
// export into the per-surah JSON files + static require-map this app bundles
// for Hifz karaoke word-sync, then regenerates the manifest across every
// reciter folder found under src/data/hifzKaraoke/ (idempotent — safe to
// re-run after adding just one more reciter's file).
//
// Usage:
//   node scripts/build-hifz-karaoke-data.mjs --reciter ar.abdurrahmaansudais --input ~/Downloads/sudais.json
//
// Input shape (QUL export, keyed "surah:ayah"):
//   {"1:1":{"surah_number":1,"ayah_number":1,"audio_url":"https://...","duration":3.0,
//           "segments":[["1","380","730"],["4","740","3082"]]}}
// `duration` may be null (tarteel.ai exports); durationMs then falls back to
// the largest segment endMs so highlighting is never disabled.
//
// Output per-surah shape (src/data/hifzKaraoke/<reciterId>/<surah>.json), keyed by
// ayah number (string), ms as numbers, word index as number:
//   {"1":{"audioUrl":"https://...","durationMs":3000,"segments":[[1,380,730],[4,740,3082]]}}

import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const ROOT = path.resolve(path.dirname(__filename), '..');
const DATA_DIR = path.join(ROOT, 'src', 'data', 'hifzKaraoke');
const MANIFEST_PATH = path.join(ROOT, 'src', 'data', 'hifzKaraokeManifest.ts');

function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === '--reciter') args.reciter = argv[++i];
    else if (argv[i] === '--input') args.input = argv[++i];
  }
  if (!args.reciter || !args.input) {
    throw new Error('Usage: node scripts/build-hifz-karaoke-data.mjs --reciter <id> --input <path-to-qul-export.json>');
  }
  return args;
}

async function convertReciter(reciterId, inputPath) {
  const raw = JSON.parse(await fs.readFile(inputPath, 'utf8'));
  const bySurah = new Map();

  for (const [key, entry] of Object.entries(raw)) {
    const surah = entry.surah_number ?? Number(key.split(':')[0]);
    const ayah = entry.ayah_number ?? Number(key.split(':')[1]);
    if (!Number.isFinite(surah) || !Number.isFinite(ayah)) continue;

    const segments = (entry.segments ?? []).map(([wordIndex, startMs, endMs]) => [
      Number(wordIndex), Number(startMs), Number(endMs),
    ]);

    // Some QUL exports (e.g. the tarteel.ai per-ayah recitations) carry a null
    // `duration`; fall back to the last word segment's endMs so durationMs is
    // never 0 — activeWordIndex() treats it as an upper bound and would blank
    // all highlighting if it were 0.
    const rawDuration = Number(entry.duration);
    const maxEndMs = segments.reduce((max, [, , endMs]) => Math.max(max, endMs), 0);
    const durationMs = Number.isFinite(rawDuration) && rawDuration > 0
      ? Math.round(rawDuration * 1000)
      : maxEndMs;

    const converted = {
      audioUrl: entry.audio_url,
      durationMs,
      segments,
    };

    if (!bySurah.has(surah)) bySurah.set(surah, {});
    bySurah.get(surah)[String(ayah)] = converted;
  }

  const outDir = path.join(DATA_DIR, reciterId);
  await fs.mkdir(outDir, { recursive: true });

  const surahNumbers = [...bySurah.keys()].sort((a, b) => a - b);
  for (const surah of surahNumbers) {
    const outPath = path.join(outDir, `${surah}.json`);
    await fs.writeFile(outPath, JSON.stringify(bySurah.get(surah)));
  }

  console.log(`${reciterId}: wrote ${surahNumbers.length} surah file(s) → ${path.relative(ROOT, outDir)}`);
  return surahNumbers;
}

async function regenerateManifest() {
  const reciterDirs = (await fs.readdir(DATA_DIR, { withFileTypes: true }))
    .filter(d => d.isDirectory())
    .map(d => d.name)
    .sort();

  const entries = [];
  for (const reciterId of reciterDirs) {
    const files = (await fs.readdir(path.join(DATA_DIR, reciterId)))
      .filter(f => f.endsWith('.json'))
      .map(f => Number(f.replace('.json', '')))
      .filter(n => Number.isFinite(n))
      .sort((a, b) => a - b);

    const surahEntries = files
      .map(surah => `    ${surah}: () => require('./hifzKaraoke/${reciterId}/${surah}.json'),`)
      .join('\n');

    entries.push(`  '${reciterId}': {\n${surahEntries}\n  },`);
  }

  const body = `// AUTO-GENERATED by scripts/build-hifz-karaoke-data.mjs — do not hand-edit.
// Regenerate by re-running the script for any reciter (it rebuilds this
// manifest from every folder present under src/data/hifzKaraoke/).
import type { RawKaraokeSurah } from './hifzKaraoke';

export const KARAOKE_SURAH_MODULES: Record<string, Partial<Record<number, () => RawKaraokeSurah>>> = {
${entries.join('\n')}
};
`;

  await fs.writeFile(MANIFEST_PATH, body);
  console.log(`Regenerated ${path.relative(ROOT, MANIFEST_PATH)} (${reciterDirs.length} reciter(s))`);
}

async function main() {
  const { reciter, input } = parseArgs(process.argv.slice(2));
  await convertReciter(reciter, input);
  await regenerateManifest();
}

main().catch(e => {
  console.error('Hifz karaoke data build failed:', e);
  process.exit(1);
});
